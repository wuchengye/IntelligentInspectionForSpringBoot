package com.bda.bdaqm.risk.model;

public class SessionDetail {
	private int rankNumber;
	private String stlSessionId;
	private String qaType;
	private String sessionContent;
	private String beginTime;
	private String endTime;
	private String fileTime;
	public int getRankNumber() {
		return rankNumber;
	}
	public void setRankNumber(int rankNumber) {
		this.rankNumber = rankNumber;
	}
	public String getStlSessionId() {
		return stlSessionId;
	}
	public void setStlSessionId(String stlSessionId) {
		this.stlSessionId = stlSessionId;
	}
	public String getQaType() {
		return qaType;
	}
	public void setQaType(String qaType) {
		this.qaType = qaType;
	}
	public String getSessionContent() {
		return sessionContent;
	}
	public void setSessionContent(String sessionContent) {
		this.sessionContent = sessionContent;
	}
	public String getBeginTime() {
		return beginTime;
	}
	public void setBeginTime(String beginTime) {
		this.beginTime = beginTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getFileTime() {
		return fileTime;
	}
	public void setFileTime(String fileTime) {
		this.fileTime = fileTime;
	}
	
	
}
